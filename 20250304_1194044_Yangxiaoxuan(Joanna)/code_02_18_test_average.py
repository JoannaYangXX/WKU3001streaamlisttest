# You will get three exam score and calculate the average and calculate the average of the three exam.Display the average exam socre with proper message.
score1 = float(input("Enter the first exam score: "))
score2 = float(input("Enter the second exam score: "))
score3 = float(input("Enter the third exam score: "))

average_score = (score1 + score2 + score3) / 3

print(f"The average exam score is: {average_score:.2f}")
